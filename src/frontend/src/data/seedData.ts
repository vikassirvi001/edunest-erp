const makeStudent = (
  n: number,
  name: string,
  dept: string,
  deptId: string,
  sem: number,
  gender: "Male" | "Female",
  fatherName: string,
  att: number,
  totalFee: number,
  paidFee: number,
  feeStatus: "Paid" | "Partial" | "Pending",
): Student => {
  const deptCode = deptId.toUpperCase().slice(0, 4);
  const year = 2024 - Math.floor((sem - 1) / 2);
  const idx = String(n).padStart(3, "0");
  return {
    id: `stu-${n}`,
    name,
    rollNumber: `AITS/${deptCode}/${year}/${idx}`,
    mobile: `98765${String(43210 + n).slice(-5)}`,
    email: `${name.toLowerCase().replace(/ /g, ".")}@student.aits.edu.in`,
    department: dept,
    departmentId: deptId,
    semester: sem,
    collegeId: "aits-001",
    fatherName,
    address: "Udaipur, Rajasthan",
    dob: `200${3 - Math.floor(n / 15)}-${String((n % 12) + 1).padStart(2, "0")}-15`,
    gender,
    bloodGroup: ["A+", "B+", "O+", "AB+", "A-", "B-"][n % 6],
    attendancePercent: att,
    totalFee,
    paidFee,
    hostFee: n % 3 === 0 ? 45000 : 0,
    busFee: n % 4 === 0 ? 12000 : 0,
    paidHostFee: n % 3 === 0 ? (feeStatus === "Paid" ? 45000 : 22500) : 0,
    paidBusFee: n % 4 === 0 ? (feeStatus === "Paid" ? 12000 : 6000) : 0,
    feeStatus,
  };
};

